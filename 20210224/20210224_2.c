/******************************************************************************

Напишете макрос swap(t, x, y), койтo променя местата на двата аргумента
от тип t.
*******************************************************************************/

#include <stdio.h>

#define swap(t, x, y) \
    t temp = x;       \
    x = y;            \
    y = temp;

int main(void)
{
    int a = 2, b = 3;
    swap(int, a, b);
    printf("a=%d,b=%d\n", a, b);
    return 0;


}
